import Head from 'next/head'
import styles from '../styles/Home.module.css'
import Tile from '../components/tile/tile'
import tiles from '../data/tiles'
import { useEffect, useState } from 'react'

export default function Home() {

  const [city, setCity] = useState('');
  useEffect(() => {
    fetch('https://ipinfo.io/geo?token=7abe1afad1fdd6').then(response => {
      response.json().then(data => {
        setCity(data.city);
      })
    });
  }, []);


  const [fact, setFact] = useState('');
  useEffect(() => {
    fetch('https://catfact.ninja/fact').then(response => {
      response.json().then(data => {
        setFact(data.fact);
      })
    });
  }, []);

  const [dogImage, setDogImage] = useState('');
  useEffect(() => {
    fetch('https://dog.ceo/api/breeds/image/random').then(response => {
      response.json().then(data => {
        setDogImage(data.message);
      })
    });
  }, []);

  return (
    <div className={styles.container}>
      <Head>
        <title>julia website</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      julia thanks you for visiting from {city}
      <main>
        <h1 className={styles.headline}>
          hello
        </h1>
        <p>I am the coolest</p>
        <div className={styles.tiles}>
          {tiles.map((tile, index) => <Tile key={index} image={tile.image} headline={tile.headline} details={tile.details} link={'posts/' + tile.link} />)}
        </div>
      </main>
      <footer>

      </footer>
    </div>
  )
}




